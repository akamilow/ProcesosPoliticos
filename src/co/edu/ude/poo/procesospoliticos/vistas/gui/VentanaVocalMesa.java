    private void btnBuscarVocalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarVocalActionPerformed
        String dni = txtDNIVocal.getText();

        // Validar que no este vacio el dni
        if (dni == null || dni.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Digite el DNI del vocal de mesa", "ERROR", JOptionPane.ERROR_MESSAGE);
            txtDNIVocal.setText("");
            txtDNIVocal.requestFocus();
            return;
        }

        // validar que el contenga la llave a buscar
        if (vocalCrud.findVocalmesaModel(Integer.parseInt(dni)) == null) {
            JOptionPane.showMessageDialog(this, "El vocal con DNI: " + dni + " no existe", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }

        VocalmesaModel v = vocalCrud.findVocalmesaModel(Integer.parseInt(dni));
        txtRolVocal.setText(v.getRol());

        // habilitar botones, y deshabilitar el boton agregar
        habilitarBotones(false, true, true, true);
    }//GEN-LAST:event_btnBuscarVocalActionPerformed

    private void btnModificarVocalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarVocalActionPerformed
        String dni = txtDNIVocal.getText();

        // Validar que no este vacio
        if (dni == null || dni.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Digite el dni del vocal de mesa", "ERROR", JOptionPane.ERROR_MESSAGE);
            txtDNIVocal.setText("");
            txtDNIVocal.requestFocus();
            return;
        }

        // validar que el contenga la llave a buscar
        if (vocalCrud.findVocalmesaModel(Integer.parseInt(dni)) == null) {
            JOptionPane.showMessageDialog(this, "El vocal con DNI: " + dni + " no existe", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String rolVocal = txtRolVocal.getText();

        // Validar que no este vacio o empty con trim, si esta vacio lanzar un mensaje JOptionPane: "Digite el nombre del partido"
        if (rolVocal == null || rolVocal.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Digite el rol del vocal", "ERROR", JOptionPane.ERROR_MESSAGE);
            txtRolVocal.setText("");
            txtRolVocal.requestFocus();
            return;
        }

        VocalmesaModel v = vocalCrud.findVocalmesaModel(Integer.parseInt(dni));
        v.setRol(rolVocal);

        try {
            vocalCrud.edit(v);
            JOptionPane.showMessageDialog(this, "El vocal con DNI: " + dni + " se actualizo con éxito", "RESULTADO", JOptionPane.WARNING_MESSAGE);
            txtDNIVocal.setText("");
            txtRolVocal.setText("");
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_btnModificarVocalActionPerformed

    private void btnEliminarVocalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarVocalActionPerformed
        String dni = txtDNIVocal.getText();

        // Validar que no este vacio el DNI
        if (dni == null || dni.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Digite el DNI del vocal de mesa", "ERROR", JOptionPane.ERROR_MESSAGE);
            txtDNIVocal.setText("");
            txtDNIVocal.requestFocus();
            return;
        }

        // validar que el contenga la llave a buscar
        if (vocalCrud.findVocalmesaModel(Integer.parseInt(dni)) == null) {
            JOptionPane.showMessageDialog(this, "El vocal con DNI: " + dni + " no existe", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int opcion = JOptionPane.showConfirmDialog(this, "¿Está seguro de eliminar el vocal de mesa con DNI: " + dni + "?", "CONFIRMACION", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE); 
        
        if (opcion == JOptionPane.YES_OPTION) {
            try {
                vocalCrud.destroy(Integer.parseInt(dni));
                JOptionPane.showMessageDialog(this, "El vocal con DNI: " + dni + " se elimino con éxito", "RESULTADO", JOptionPane.WARNING_MESSAGE);
                txtDNIVocal.setText("");
                txtRolVocal.setText("");
            } catch (Exception e) {
                System.out.println(e.getMessage());
            }
        }
    }//GEN-LAST:event_btnEliminarVocalActionPerformed

