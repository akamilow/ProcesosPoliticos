    private void btnAgregarVocalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarVocalActionPerformed
        // Datos de formulario, crear un apoderado de mesa
        String dni = txtDNIVocal.getText();

        // Validar que no este vacio el dni
        if (dni == null || dni.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Digite el dni del vocal", "ERROR", JOptionPane.ERROR_MESSAGE);
            txtDNIVocal.setText("");
            txtDNIVocal.requestFocus();
            return;
        }

        String rolVocal = txtRolVocal.getText();

        // Validar que no este vacio el rol
        if (rolVocal == null || rolVocal.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Digite el rol del vocal de mesa", "ERROR", JOptionPane.ERROR_MESSAGE);
            txtRolVocal.setText("");
            txtRolVocal.requestFocus();
            return;
        }

        // Validar que el dni del ciudadano exista
        CiudadanoModelJpaController ciudadanoCrud = new CiudadanoModelJpaController(con);
        CiudadanoModel c = ciudadanoCrud.findCiudadanoModel(Integer.parseInt(dni));
        if (c == null) {
            JOptionPane.showMessageDialog(this, "El ciudadano con DNI: " + dni + " no existe", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Crear un objeto de tipo VocalMesa
        VocalmesaModel v = new VocalmesaModel();
        v.setDni(Integer.parseInt(dni));
        v.setRol(rolVocal);

        try {
            vocalCrud.create(v);
            // Mensaje de confirmacion
            int totalVocalAlmacenados = vocalCrud.getVocalmesaModelCount();
            String msg = "El apoderado de mesa: " + c.getNombre() + " se guardo con éxito";
            msg += "\n" + " TOTAL: " + totalVocalAlmacenados;
            JOptionPane.showMessageDialog(this, msg, "RESULTADO", JOptionPane.WARNING_MESSAGE); 
            txtDNIVocal.setText("");          
            txtRolVocal.setText("");
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_btnAgregarVocalActionPerformed

