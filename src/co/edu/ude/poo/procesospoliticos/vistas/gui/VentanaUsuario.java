    private void btnBuscarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarUsuarioActionPerformed
        String dni = txtDNIUsuario.getText();

        // Validar que no este vacio el dni
        if (dni == null || dni.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Digite el DNI del ciudadano", "ERROR", JOptionPane.ERROR_MESSAGE);
            txtDNIUsuario.setText("");
            txtDNIUsuario.requestFocus();
            return;
        }

        // validar que el contenga la llave a buscar
        if (usuariosCrud.findUsuarioModel(Integer.parseInt(dni)) == null) {
            JOptionPane.showMessageDialog(this, "El usuario con DNI: " + dni + " no existe", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // se recupera el objeto para mostrarlo en los campos del formulario
        UsuarioModel u = usuariosCrud.findUsuarioModel(Integer.parseInt(dni));
        txtContrasena.setText(u.getContrasena());

        // habilitar botones, y deshabilitar el boton agregar
        habilitarBotones(false, true, true, true);
    }//GEN-LAST:event_btnBuscarUsuarioActionPerformed

    private void btnModificarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarUsuarioActionPerformed
        String dni = txtDNIUsuario.getText();

        // Validar que no este vacio
        if (dni == null || dni.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Digite el dni del ciudadano", "ERROR", JOptionPane.ERROR_MESSAGE);
            txtDNIUsuario.setText("");
            txtDNIUsuario.requestFocus();
            return;
        }

        // validar que el contenga la llave a buscar
        if (usuariosCrud.findUsuarioModel(Integer.parseInt(dni)) == null) {
            JOptionPane.showMessageDialog(this, "El usuario con DNI: " + dni + " no existe", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String usuarioContrasena = txtContrasena.getText();

        // Validar que no este vacio o empty con trim, si esta vacio lanzar un mensaje JOptionPane: "Digite el nombre del partido"
        if (usuarioContrasena == null || usuarioContrasena.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Digite la contraseña del ciudadano", "ERROR", JOptionPane.ERROR_MESSAGE);
            txtContrasena.setText("");
            txtContrasena.requestFocus();
            return;
        }

        // obten el objeto apoderado de la base de datos
        UsuarioModel u = usuariosCrud.findUsuarioModel(Integer.parseInt(dni));
        u.setDni(Integer.parseInt(dni));
        u.setContrasena(usuarioContrasena);

        try {
            usuariosCrud.edit(u);
            JOptionPane.showMessageDialog(this, "El ciudadano con DNI: " + dni + " se actualizo con éxito", "RESULTADO", JOptionPane.WARNING_MESSAGE);
            txtDNIUsuario.setText("");
            txtContrasena.setText("");
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_btnModificarUsuarioActionPerformed

    private void btnEliminarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarUsuarioActionPerformed
        String dni = txtDNIUsuario.getText();

        // Validar que no este vacio o empty con trim, si esta vacio lanzar un mensaje JOptionPane: "Digite el id del partido"
        if (dni == null || dni.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Digite el DNI del ciudadano", "ERROR", JOptionPane.ERROR_MESSAGE);
            txtDNIUsuario.setText("");
            txtDNIUsuario.requestFocus();
            return;
        }

        // validar que el contenga la llave a buscar
        if (usuariosCrud.findUsuarioModel(Integer.parseInt(dni)) == null) {
            JOptionPane.showMessageDialog(this, "El usuario con DNI: " + dni + " no existe", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int opcion = JOptionPane.showConfirmDialog(this, "¿Está seguro de eliminar el apoderado con DNI: " + dni + "?", "CONFIRMACION", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE); 
        
        if (opcion == JOptionPane.YES_OPTION) {
            try {
                usuariosCrud.destroy(Integer.parseInt(dni));
                JOptionPane.showMessageDialog(this, "El usuario con DNI: " + dni + " se elimino con éxito", "RESULTADO", JOptionPane.WARNING_MESSAGE);
                txtDNIUsuario.setText("");
                txtContrasena.setText("");
            } catch (Exception e) {
                System.out.println(e.getMessage());
            }
        }
    }//GEN-LAST:event_btnEliminarUsuarioActionPerformed

