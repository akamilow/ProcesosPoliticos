    private void btnAgregarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarUsuarioActionPerformed
        // Datos de formulario, crear un apoderado de mesa
        String dni = txtDNIUsuario.getText();

        // Validar que no este vacio el dni
        if (dni == null || dni.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Digite el dni del apoderado", "ERROR", JOptionPane.ERROR_MESSAGE);
            txtDNIUsuario.setText("");
            txtDNIUsuario.requestFocus();
            return;
        }  

        String usuarioContrasena = txtContrasena.getText();

        // Validar que no este vacio o empty con trim, si esta vacio lanzar un mensaje JOptionPane: "Digite el nombre del partido"
        if (usuarioContrasena == null || usuarioContrasena.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Digite la contraseña del apoderado", "ERROR", JOptionPane.ERROR_MESSAGE);
            txtContrasena.setText("");
            txtContrasena.requestFocus();
            return;
        }

        // Crear objeto apoderado de mesa
        UsuarioModel u = new UsuarioModel();
        u.setDni(Integer.parseInt(dni));
        u.setContrasena(usuarioContrasena);
            
        
        try {
            usuariosCrud.create(u);
            // Mensaje de confirmacion
            int totalUsuariosAlmacenados = usuariosCrud.getUsuarioModelCount();
            String msg = "El usuario se guardo con éxito";
            msg += "\n" + " TOTAL: " + totalUsuariosAlmacenados;
            JOptionPane.showMessageDialog(this, msg, "RESULTADO", JOptionPane.WARNING_MESSAGE); 
            txtDNIUsuario.setText("");          
            txtContrasena.setText("");
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_btnAgregarUsuarioActionPerformed

